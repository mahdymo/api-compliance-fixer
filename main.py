@app.post("/api/request-access")
async def request_access(
    upload_token: str = Form(...),
    name:         str = Form(...),
    email:        str = Form(...),
    company:      str = Form(""),
    role:         str = Form(""),
    how_heard:    str = Form(""),
    use_case:     str = Form(""),
):
    """
    Collect user details and log to Google Forms.
    No token is issued here — the team reviews and sends one manually.
    """
    entry = pending_uploads.get(upload_token)
    if not entry:
        raise HTTPException(404, "Upload not found or expired. Please re-upload your file.")

    name  = name.strip()
    email = email.strip().lower()
    if not name:
        raise HTTPException(400, "Name is required.")
    if not email or "@" not in email:
        raise HTTPException(400, "A valid email address is required.")

    changelog = entry.get("changelog", {})

    await _submit_to_gform({
        "timestamp":       datetime.now(timezone.utc).isoformat(),
        "name":            name,
        "email":           email,
        "company":         company.strip(),
        "role":            role.strip(),
        "how_heard":       how_heard,
        "use_case":        use_case,
        "collection_name": _col_name(entry),
        "frameworks":      ", ".join(entry["frameworks"]),
        "total_changes":   changelog.get("total_changes", 0),
        "format":          entry.get("format", ""),
    })

    return {
        "ok":      True,
        "message": "Request received. Our team will review and send your access token shortly.",
    }


