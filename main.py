@app.get("/admin/generate")
async def generate_token(key: str = "", label: str = "", email: str = ""):
    """Generate a new free-standing access token. Returns JSON."""
    _require_admin(key)
    if not label:
        raise HTTPException(400, "Label is required (who is this token for?).")

    token = secrets.token_urlsafe(32)
    issued_tokens[token] = {
        "label":      label,
        "email":      email.strip().lower(),
        "created_at": datetime.now(timezone.utc).isoformat(),
        "use_count":  0,
        "last_used":  "never",
    }
    print(f"[ADMIN] Token generated for '{label}' ({email}): {token[:12]}…")
    return {
        "access_token": token,
        "label":        label,
        "email":        email,
        "created_at":   issued_tokens[token]["created_at"],
    }


@app.get("/admin/tokens")
async def list_tokens(key: str = ""):
    """List all issued tokens. Returns JSON."""
    _require_admin(key)
    return [
        {"token_prefix": tok[:12] + "…", **meta}
        for tok, meta in issued_tokens.items()
    ]


