@app.post("/api/redeem")
async def redeem_token(
    access_token: str = Form(...),
    upload_token: str = Form(...),
):
    """
    Validate an admin-issued access token against the current upload.
    Access token is free-standing (not tied to email) and reusable.
    Returns full changelog + a fresh single-use download token.
    """
    token_entry = issued_tokens.get(access_token.strip())
    if not token_entry:
        raise HTTPException(404, "Invalid or expired access token.")

    upload_entry = pending_uploads.get(upload_token)
    if not upload_entry:
        raise HTTPException(
            410,
            "Your file session has expired — please re-upload your file and try again.",
        )

    # Record usage
    token_entry["use_count"] += 1
    token_entry["last_used"]  = datetime.now(timezone.utc).isoformat()

    # Issue a fresh single-use download token
    dl_token = secrets.token_urlsafe(32)
    download_tokens[dl_token] = upload_entry

    return {
        "ok":             True,
        "download_token": dl_token,
        "changelog":      upload_entry["changelog"],   # full, unredacted
    }


